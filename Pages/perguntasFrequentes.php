                <h3>Perguntas Frequentes</h3>

               <p>
                    <strong>1- O que é consórcio?</strong>  <br><br>
                    Consórcio é a reunião de pessoas naturais e/ou jurídicas em grupo, com prazo de duração e número de cotas previamente determinados, promovida por 
                    administradora de consórcio, com a finalidade de propiciar a seus integrantes, de forma isonômica, a aquisição de bens ou serviços, por meio de 
                    autofinanciamento. O contrato de participação em grupo de consórcio, por adesão, poderá ter como referência bem móvel, imóvel ou serviço de qualquer 
                    natureza. Todos os grupos da administradora são independentes. Recursos de um grupo não podem ser transferidos para outro, nem se confundem com o 
                    patrimônio das administradoras. fonte : Banco Central do Brasil.

               </p><br><br>

               <p>
                    <strong>2- Calendário de vencimento e pagamento das parcelas. </strong>
                    Todos os meses, você vai receber um demonstrativo contendo uma descrição completa da sua posição no grupo e também informações como: datas das 
                    assembleias e vencimentos das mensalidades, além de poder efetuar o pagamento em qualquer agência bancária até a data do vencimento. 
                </p><br><br>

                <p>
                    <strong>3-     Como é feito o cálculo do valor das prestações? </strong>
                </p>

                <p>
                A prestação corresponde à soma das importâncias referentes à parcela destinada ao fundo comum do grupo, à taxa de administração e às demais obrigações 
estabelecidas no contrato. O valor destinado ao fundo comum corresponde ao valor do bem ou do serviço referenciado no contrato, por ocasião da 
constituição do grupo, dividido pelo número de prestações. Esse valor, juntamente com os recursos mensais dos demais consorciados, é utilizado para a 
realização das contemplações do grupo. Havendo aumento do preço do bem, o valor pago mensalmente pelos consorciados (contemplados e não 
contemplados) a título de fundo comum, deve ser ajustado na mesma proporção do aumento do bem/serviço, visando arrecadar recursos suficientes para a 
contemplação de todos os participantes do grupo. A taxa de administração corresponde ao valor pago às administradoras de consórcio pela gestão e 
administração do grupo. O percentual da taxa de administração deve estar definido no contrato de adesão. O contrato pode prever outros valores, como o 
pagamento de seguros e uma taxa referente ao fundo de reserva. O fundo de reserva, se estabelecido no grupo de consórcio, somente poderá ser utilizado 
nas situações previstas nos normativos vigentes. Fonte: Banco Central do Brasil.
                </p><br><br>

                <p>
                    <strong>4-    Como ofertar seu lance? </strong>

                    Você deve efetuar o lance sempre em número de parcelas de, no mínimo 10% do seu saldo devedor e no máximo, 100% do número de parcelas faltantes para 
o término do seu grupo. Caso o lance vencedor pertença a uma cota que tenha sido sorteada na mesma assembleia ou não esteja habilitada a participar (com 
mensalidades em atraso), será pesquisado o segundo maior lance do grupo e assim sucessivamente Oferte seu lance Você pode ofertar lance através do site 
ou da central de atendimento das administradoras.


                </p><br><br>

                <p>
                    <strong>5-    
                    Como saber se a empresa é autorizada a operar pelo Banco Central como administradora de consórcio? </strong>
A relação completa das administradoras de consórcio autorizadas a funcionar pelo Banco Central do Brasil está disponível em nosso site, seguindo: "Sistema 
Financeiro Nacional>Informações cadastrais e contábeis>Informações cadastrais>Relação de instituições em funcionamento no país (transferência de 
arquivos)". Para informações sobre administradoras sem grupos em andamento, consulte a nossa Central de Atendimento ao Público. Fonte: Banco Central 
do Brasil



                </p><br><br>

                <p>
                    <strong>6 - Como são formados os grupos? </strong>

                    Para formar um grupo, é preciso que o número de participantes seja múltiplo do número de meses previsto para a duração do plano, e assim é definido o 
número de contemplações. Por exemplo: em um grupo de 60 meses, com 360 participantes, o número previsto de contemplações mensais, por sorteio e 
lances, serão de 6 (seis) unidades, sempre de acordo com a disponibilidade financeira do grupo. 
                </p><br><br>

                <p>
                    <strong>7- Depois de constatar que a administradora está autorizada, ainda é necessário tomar outras precauções? </strong>

                    Sim. É recomendável ligar também para os órgãos de defesa do consumidor de sua região para ver se há reclamações contra a empresa. Além disso, é muito 
importante ler cuidadosamente seu contrato de adesão antes de assiná-lo e efetuar qualquer pagamento. Não efetue pagamentos em dinheiro. Os 
pagamentos devem ser feitos em cheques não à ordem e nominativos à administradora de consórcios, ou de outra forma que lhe permita comprovar o 
pagamento realizado. Fonte: Banco Central do Brasil
 
                </p><br><br>

                <p>
                    <strong>8- E o que é contemplação? </strong>

                    Contemplação é a atribuição ao consorciado do crédito para a aquisição de bem ou serviço. O crédito corresponde ao valor atualizado do bem ou do serviço 
na data da sua contemplação. No caso do consorciado excluído, o crédito corresponde ao valor a ser pago para a restituição das parcelas pagas, e é 
considerado crédito parcial. A contemplação pode ocorrer por sorteio ou por lance e é realizada nas assembleias gerais de contemplação, geralmente 
mensais. Fonte: Banco Central do Brasil
 
                </p><br><br>

                <p>

                    <strong>9- E se eu quiser comprar um bem de valor diferente daquele que está no meu contrato? </strong>

                    Respeitando os segmentos, não há problema. Para adquirir um bem de maior valor, você ficará responsável pelo pagamento da diferença de preço. Caso você
decida adquirir bem, conjunto de bens, serviço ou conjunto de serviços com preço inferior ao valor do respectivo crédito, a diferença deve ser utilizada, a seu 
critério, para: a. pagamento de obrigações financeiras, vinculadas ao bem ou serviço, observado o limite total de 10% do valor do crédito objeto da 
contemplação, relativamente às despesas com transferência de propriedade, tributos, registros cartoriais, instituições de registro e seguros; b. quitação das 
prestações vincendas na forma estabelecida no contrato; d. devolução do crédito em espécie ao consorciado quando suas obrigações financeiras para com o
 grupo estiverem integralmente quitadas. Fonte: Banco Central do Brasil
 
                </p><br><br>

                <p>
                    <strong>10- Existe desconto para quitação? </strong>

                    No consórcio o desconto não acontece, pois não cobramos juros, mas sim, taxa fixa de administração

                </p><br><br>

                <p>
                    <strong>11- Existe um prazo para a contemplação?</strong>

                    A contemplação de cada consorciado dependerá da seleção de sua cota nas assembleias mensais, que poderá ocorrer desde a primeira assembleia até a 
última. A realização dos sorteios ocorre igualmente entre todos os participantes do grupo
 
                </p><br><br>

                <p>
                    <strong>12- Há prazo para a aquisição do bem depois da contemplação? </strong>

                    E o que acontece se o bem aumenta de preço depois que eu for contemplado?
Não há prazo para a aquisição do bem após a contemplação. Mas, uma vez contemplado, o valor correspondente ao crédito será apartado dos recursos do 
fundo comum do grupo e receberá rendimentos de aplicação financeira até o momento da sua utilização. Se houver aumento do preço do bem/serviço durante
 esse período e esses recursos forem insuficientes para a sua aquisição, o contemplado terá que arcar com a diferença, caso opte ainda pelo bem/serviço 
referenciado em seu contrato. Fonte: Banco Central do Brasil
 
                </p><br><br>

                <p>
                    <strong>13- Moro no exterior, posso comprar um consórcio? </strong>

                    Pode sim! Na compra da cota você deverá informar um endereço de correspondência no Brasil e nomear um representante legal para representá-lo, quando 
necessário, perante o consórcio, sendo que o acompanhamento do plano poderá ser feito através de nosso site ou através de um representante autorizado.
 
                </p><br><br>

                <p>
                    <strong>14- O Banco Central fiscaliza a atuação das administradoras de consórcio? </strong>

                    Sim. Essa é uma atribuição do Banco Central do Brasil, conforme disposto na Lei 11.795, de 2008. Fonte: Banco Central do Brasil

                </p><br><br>

                <p>
                    <strong>15- O que é contemplação? </strong>

                    Quando uma cota é sorteada para receber a carta de crédito referente ao bem ou serviço. Existem contemplações por sorteio e por lances
 
                </p><br><br>

                <p>
                    <strong>16- O que é fundo comum?</strong>

                    Parte da mensalidade relativa ao valor da carta de crédito, dividida pelo número de parcelas do plano. Destina-se a cobrir o valor dos bens entregues do mês

                </p><br><br>

                <p>
                    <strong>17- O que é lance? </strong>

                    É a forma do consorciado antecipar sua contemplação
                </p><br><br>

                <p>
                    <strong>18- O que é pedra-chave? </strong>

                    É o nome dado a bola sorteada pelo globo no dia da assembleia
                </p><br><br>

                <p>
                    <strong>19- O que é Seguro Prestamista?</strong>

                    O seguro prestamista é um seguro de vida em grupo, que assegura a tranquilidade da família e do grupo, no caso de sinistro do consorciado. Desta forma, caso 
ocorra algo ao segurado que esteja dentro dos critérios de cobertura, a cota será quitada. A cobertura se dará para os casos de morte natural ou acidental, 
invalidez permanente e total, por acidente, tendo como objetivo a liquidação/amortização do débito da operação de consórcio. Importante: nesta modalidade 
não há cobertura para desemprego mediante análise da seguradora.
 
                </p><br><br>

                <p>
                    <strong>20- O que é taxa de administração?</strong>

                    Cobrada mensalmente, a taxa cobre os custos de administração do consórcio (realização das assembleias, controle dos grupos, emissão dos boletos
bancários, sistemas, etc). O percentual varia de acordo com a modalidade de consórcio e não é devolvida em caso de desistência


                </p><br><br>

                <p>
                    <strong>21- O que é uma administradora de consórcio? </strong>

                    Empresa autorizada pelo Banco Central a formar e administrar recursos de grupos de consórcios para a aquisição de bens ou serviços
                </p><br><br>

                <p>
                    <strong>22- O que é uma assembleia? </strong>

                    Evento mensal onde são apurados os contemplados por lance e onde é feito o sorteio da pedra-chave que indicará os demais contemplados do mês de 
cada grupo

                </p><br><br>

                <p>
                    <strong>23- O que é uma cota? </strong>

                    É a parte que cabe a cada consorciado e o número que o identifica no grupo

                </p><br><br>

                <p>
                    <strong>24- O que eu posso comprar com meu crédito quando eu for contemplado? </strong>

                    Quando contemplado, você poderá adquirir, em fornecedor, vendedor ou prestador de serviço que melhor lhe convier: a. veículo automotor, aeronave, 
embarcação, máquinas e equipamentos, se o contrato estiver referenciado em qualquer bem mencionado nesta alínea; b. qualquer bem móvel ou conjunto de 
bens móveis, novos, excetuados os referidos na alínea anterior, se o contrato estiver referenciado em bem móvel ou conjunto de bens móveis não mencionados 
naquela alínea; c. qualquer bem imóvel, construído ou na planta, inclusive terreno, ou ainda optar por construção ou reforma, desde que em município em que a 
administradora opere ou, se autorizado pela administradora, em município diverso, se o contrato estiver referenciado em bem imóvel; d. serviço, se o contrato 
estiver referenciado em serviço. Assim, você poderá comprar qualquer bem que esteja no mesmo segmento definido no seu contrato, no fornecedor que você 
escolher. Por isso, é possível um consorciado de um grupo de motocicletas comprar um automóvel com seu crédito. A administradora não pode obrigá-lo a 
comprar o bem escolhido por você em uma revenda indicada por ela. A escolha é sua. A administradora só pode transferir recursos a terceiros após ter sido 
comunicada pelo consorciado de sua opção. O contrato de grupo para a aquisição de bem imóvel poderá estabelecer a aquisição de imóvel em 
empreendimento imobiliário. Além disso, o consorciado contemplado pode realizar a quitação total de financiamento, de sua titularidade, nas condições 
previstas no contrato, de bens e serviços possíveis de serem adquiridos por meio do crédito obtido. Fonte: Banco Central do Brasil

                </p><br><br>

                <p>
                    <strong>25- Podem me cobrar taxa de adesão?</strong>

                    Não, atualmente não existe taxa de adesão. Contudo, quando você entra em um grupo de consórcio, a administradora poderá cobrar além da primeira 
mensalidade ou prestação, antecipação de recursos relativos à taxa de administração, visando à cobertura de despesas imediatas vinculadas à venda de 
cotas do grupo e remuneração de representantes e corretores, devendo tais valores serem deduzidos do valor total da taxa de administração durante o prazo 
de duração do grupo. Tudo isso deve estar previsto no contrato de adesão. Fonte: Banco Central do Brasil
                </p><br><br>

                <p>
                    <strong>26- Posso quitar um financiamento com o crédito recebido na contemplação?</strong>

                    Sim, a legislação atual permite a quitação total de financiamento com o crédito recebido na contemplação, conforme indicado na resposta nº 18. Contudo, para
 grupos formados até 5.2.2009 e que não tenham aderido à nova regulamentação, não é permitido tal procedimento. Fonte: Banco Central do Brasil
                </p><br><br>

                <p>
                    <strong>27- Posso utilizar meu FGTS no consórcio de imóveis ? </strong>

                    A utilização do FGTS é autorizada pela Caixa Econômica Federal para pagamento de lance em consórcio de imóveis, bem como para complementação do 
valor do crédito, desde que observadas as condições abaixo (conforme circular 249 da CEF): - utilização exclusiva para aquisição de imóvel residencial, urbano, 
concluído, para moradia própria do trabalhador; - o trabalhador deve ter, no mínimo 3 anos de trabalho sob o regime do FGTS (consecutivos ou não); - o 
trabalhador não seja proprietário ou promitente de outro imóvel residencial concluído ou em construção, na mesma localidade onde pretende adquirir, no atual 
município de residência ou no município onde exerce sua ocupação principal, incluindo seus limites integrantes da mesma Região Metropolitana. Os detalhes 
do procedimento serão fornecidos por nossa filial no momento da contemplação

                </p><br><br>

                <p>
                    <strong>28- Quando começa o grupo de consórcio? </strong>

                    Considera-se constituído o grupo de consórcio com a realização da primeira assembleia, que será designada pela administradora de consórcio quando houver 
adesões em número e condições suficientes para assegurar a viabilidade econômico-financeira do empreendimento. Se a administradora não aprovar a 
constituição do grupo até noventa dias depois de sua adesão, deverá devolver ao consorciado, integralmente, todos os valores pagos, acrescidos dos 
rendimentos líquidos provenientes de sua aplicação financeira. Fonte: Banco Central do Brasil
 
                </p><br><br>

                <p>
                    <strong>29- Quando eu for contemplado, posso pegar meu crédito em dinheiro?</strong>

                    A finalidade do consórcio é a aquisição de bens, conjunto de bens, serviços ou conjunto de serviços. No entanto, é possível receber o valor do crédito em 
espécie, mediante quitação de suas obrigações para com o grupo, caso ainda não tenha utilizado o respectivo crédito decorridos 180 dias da contemplação. 
Além disso, dentro de sessenta dias, contados da data da realização da última assembleia de contemplação do grupo de consórcio, a administradora deve 
comunicar aos consorciados que não tenham utilizado os respectivos créditos, por meio de carta com Aviso de Recebimento, telegrama ou correspondência 
eletrônica, que os valores estão à disposição para recebimento em espécie. Fonte: Banco Central do Brasil

                </p><br><br>

                <p>
                    <strong>30- Que garantias a administradora pode exigir dos consorciados? </strong>

                    As garantias iniciais em favor do grupo devem recair sobre o bem adquirido por meio do consórcio. No caso de consórcio de bem imóvel, é facultado à 
administradora aceitar em garantia outro imóvel de valor suficiente para assegurar o cumprimento das obrigações pecuniárias do contemplado em relação ao 
grupo. Além disso, admitem-se garantias reais ou pessoais, sem vinculação ao bem referenciado, no caso de consórcio de serviço de qualquer natureza, ou 
]quando, na data de utilização do crédito, o bem estiver sob produção, incorporação ou situação análoga. A administradora pode exigir garantias 
complementares proporcionais ao valor das prestações vincendas. Fonte: Banco Central do Brasil
                </p><br><br>

                <p>
                    <strong>31- Quitação gera contemplação? </strong>

                    A quitação não gera contemplação automática, pois ela ocorrerá exclusivamente por sorteio ou lance. Caso você queira quitar a sua cota poderá ofertar um 
lance máximo com possibilidade de contemplação e quitação da cota
                </p><br><br>

                <p>
                    <strong>32- Tenho que dar entrada para começar a participar do consórcio? </strong>

                    O valor da entrada significa o pagamento da primeira parcela

                </p><br><br>

                

